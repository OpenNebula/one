module OpenNebula

    # Service Template pool
    class ServiceTemplatePool < DocumentPoolJSON

        DOCUMENT_TYPE = 101

        def factory(element_xml)
            s_template = OpenNebula::ServiceTemplate.new(element_xml, @client)
            s_template.load_body
            s_template
        end

    end

end
